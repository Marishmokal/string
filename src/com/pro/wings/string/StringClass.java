package com.pro.wings.string;

public class StringClass {

	public static void main(String[] args) {

		String s1 = "India";
		String s2= "is great";
					
		System.out.println("Length :"+s1.length());  //length()
		System.out.println("charAt() :"+s1.charAt(3));  //charAt()
		System.out.println("Substring1() :"+s2.substring(4)); 
		System.out.println("Substring2() :"+s2.substring(1,5));
	}

}
